#include <libft.h>

char	*ft_strrchr(const char *s, int c)
{
	char	*str;

	str = (char *)s;
	while (*str != '\0')
		str++;
	while (*str != c)
	{
		str--;
		if (*str == '\0')
			return (NULL);
	}
	return (str);
}
